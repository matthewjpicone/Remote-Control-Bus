// Function to select bus number on multiplexer
void DESTOSELECT(uint8_t bus)
{
  Wire.beginTransmission(0x72);  // TCA9548A address is 0x72
  Wire.write(1 << bus);          // send byte to select bus
  Wire.endTransmission();
}

